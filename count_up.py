def count_up(start, stop):
    """Print all numbers from start up to and including stop.

    For example:

        count_up(5, 7)

   should print:

        5
        6
        7
    """

    # YOUR CODE HERE

    num = start  # Initialize num with the starting value
    while num <= stop:
        print(num)
        num = num + 1  # Increment num by 1 i


count_up(5, 7)

